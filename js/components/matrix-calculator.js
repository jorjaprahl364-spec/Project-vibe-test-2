/**
 * Нумерологическое сложение (приведение к 1-22).
 * @param {number} num
 * @returns {number}
 */
export function reduceArcana(num) {
  if (num <= 0) return 22;
  let val = num;
  while (val > 22) {
    val = String(val)
      .split('')
      .reduce((acc, d) => acc + parseInt(d, 10), 0);
  }
  return val;
}

/**
 * Рассчитывает 3 ключевые энергии на основе дня, месяца и года рождения.
 * @param {number} day
 * @param {number} month
 * @param {number} year
 * @returns {{entry: number, main: number, debt: number}}
 */
export function calculateMoneyMatrix(day, month, year) {
  const dayArc = reduceArcana(day);
  const monthArc = reduceArcana(month);
  
  // Считаем Аркан года
  const yearSum = String(year)
    .split('')
    .reduce((acc, d) => acc + parseInt(d, 10), 0);
  const yearArc = reduceArcana(yearSum);

  // 1. Вход в денежный канал
  const entry = reduceArcana(dayArc + monthArc);
  
  // 2. Главная денежная энергия
  const main = reduceArcana(monthArc + yearArc);
  
  // 3. Кармический долг по деньгам
  const debt = reduceArcana(dayArc + monthArc + yearArc);

  return { entry, main, debt };
}
